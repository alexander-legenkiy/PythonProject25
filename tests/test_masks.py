def test_get_mask_account():
    assert get_mask_account(str(73654108430135874305)) == "**4305"  # """#проверка работы функии
    # на корректность работы"""
    assert get_mask_account(str(73654108430135874305))[-4:] == "4305"  # """#проверка работы функии
    # на корректность работы"""
    assert get_mask_account(str(73654108430135874305))[0:2] == "**"  # """#проверка работы функии
    # на корректность работы"""
    with pytest.raises(ValueError):
        get_mask_account("7365410A430135874305")  # """#проверка работы функии в случае присутствия буквы
        # при вводе номера счёта"""
    with pytest.raises(ValueError):
        get_mask_account("736541084301358743051")  # """#проверка работы функии в случае присутствия буквы
        # при вводе 21 цифры для номера счёта, вместо корректных 20"""
